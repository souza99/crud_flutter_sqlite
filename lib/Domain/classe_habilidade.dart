

class ClasseHabilidade {
  late int _id;
  late int _classeId;
  late int _habilidadeId;

  int get id => _id;

  set id(int id) {
    _id = id;
  }

  int get classeId => _classeId;

  set classeId(int classeId) {
    _classeId = classeId;
  }

  int get habilidadeId => _habilidadeId;

  set habilidadeId(int habilidadeId) {
    _habilidadeId = habilidadeId;
  }

}